      SUBROUTINE DATCNV(NP,NE,MWRK,
     *                  NPINLT,NPWALL,NPSYMT,NPFREE,NPCCL ,NPBODY,
     *                  NPINT ,
     *                  LPINLT,LPWALL,LPSYMT,LPFREE,LPCCL1,LPCCL2,
     *                  LPBODY,LPINT1,
     *                  X,Y,Z,XD,YD,ZD,U,V,W,PN,P,
     *                  LPCNV,LECNV,WRK01,IERR,IUT0)
C
      IMPLICIT NONE
C
      INTEGER*4 NP,NE,MWRK
C     LIST DATA
      INTEGER*4 NPINLT,NPWALL,NPSYMT,NPFREE,NPCCL ,NPBODY,
     *          NPINT
      INTEGER*4 LPINLT(NPINLT),LPWALL(NPWALL),LPSYMT(NPSYMT),
     *          LPFREE(NPFREE),LPCCL1(NPCCL ),LPCCL2(NPCCL ),
     *          LPBODY(NPBODY),LPINT1(NPINT )
C
C     FLOW FILED DATA
      REAL*4    X(NP),Y(NP),Z(NP),U(NP),V(NP),W(NP),PN(NP),P(NE)
      REAL*8    XD(NP),YD(NP),ZD(NP)
C
C     CONVERSION TABLE
      INTEGER*4 LPCNV(NP),LECNV(NE)
C
C     WORK
      REAL*4    WRK01 (MWRK)
      INTEGER*4 IERR,IUT0
C
CC
CC    [1] CONVERT LIST DATA
CC
      IF (NPINLT.GE.1) CALL LSTCNV(NP,NPINLT,LPINLT,LPCNV,IERR,IUT0)
      IF (NPWALL.GE.1) CALL LSTCNV(NP,NPWALL,LPWALL,LPCNV,IERR,IUT0)
      IF (NPSYMT.GE.1) CALL LSTCNV(NP,NPSYMT,LPSYMT,LPCNV,IERR,IUT0)
      IF (NPFREE.GE.1) CALL LSTCNV(NP,NPFREE,LPFREE,LPCNV,IERR,IUT0)
      IF (NPCCL .GE.1) CALL LSTCNV(NP,NPCCL ,LPCCL1,LPCNV,IERR,IUT0)
      IF (NPCCL .GE.1) CALL LSTCNV(NP,NPCCL ,LPCCL2,LPCNV,IERR,IUT0)
      IF (NPBODY.GE.1) CALL LSTCNV(NP,NPBODY,LPBODY,LPCNV,IERR,IUT0)
      IF (NPINT .GE.1) CALL LSTCNV(NP,NPINT ,LPINT1,LPCNV,IERR,IUT0)
CC
CC    [2] CONVERT ATTRIBUTE DATA
CC
CC    [3] CONVERT FLOW FILED DATA
CC
      CALL RELCNV(NP,NP,X     ,LPCNV, WRK01,IERR,IUT0)
      CALL RELCNV(NP,NP,Y     ,LPCNV, WRK01,IERR,IUT0)
      CALL RELCNV(NP,NP,Z     ,LPCNV, WRK01,IERR,IUT0)
      CALL DBLCNV(NP,NP,XD    ,LPCNV,       IERR,IUT0)
      CALL DBLCNV(NP,NP,YD    ,LPCNV,       IERR,IUT0)
      CALL DBLCNV(NP,NP,ZD    ,LPCNV,       IERR,IUT0)
      CALL RELCNV(NP,NP,U     ,LPCNV, WRK01,IERR,IUT0)
      CALL RELCNV(NP,NP,V     ,LPCNV, WRK01,IERR,IUT0)
      CALL RELCNV(NP,NP,W     ,LPCNV, WRK01,IERR,IUT0)
      CALL RELCNV(NP,NP,PN    ,LPCNV, WRK01,IERR,IUT0)
      CALL RELCNV(NE,NE,P     ,LECNV, WRK01,IERR,IUT0)
C
      RETURN
      END

